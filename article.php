      <h1>
        Arts et sciences cognitives
      </h1>
      <p>
        Il y a une dizaine d'années, dans Being There1 , Andy Clark soulignait à quel point il est tentant pour le sens commun de considérer le cerveau comme un « alien » (un étranger) introduit dans une enveloppe de chair. Cependant il poursuivait avec un exemple qui contredisait complètement cette idée : celui de la nage des dauphins et des thons, qui montre que le cerveau de ces mammifères actionne des réseaux en chaîne exploitant directement des systèmes nerveux secondaires. Et ce, pour leur permettre de se propulser en fonction de l'environnement plus vite qu'il ne semblerait possible de le faire avec leur masse musculaire. En d'autres termes, si ces mammifères peuvent se mouvoir si vite, c'est qu'ils intègrent dans l'immédiateté la perception de leur environnement, avec ce qu’on pourrait appeler un réseau périphérique, qui n’envoie qu’une information synthétique sommaire au cerveau central. Ce qui est sans doute le cas chez l'homme jusqu'à un certain point, et ce qui démontre que les divisions chronologiques traditionnelles que l'on accordait à la perception, à la cognition et à l'action sont aujourd'hui en passe d’être complètement révisées.
      </p>
      <a class="pic-ref" href="#img-1" to="#img-1">[FIG 1]</a>
      <p>
        Je cite cet exemple significatif, car l'avancée majeure des recherches en neurosciences depuis le début des années 90 a fait naître un mouvement de pensée qui a choisi la terminologie d’embodiement of the meaning (incorporation du sens) pour se définir, et qui regroupe toutes les hypothèses sur une pensée dont l’exercice passe avant tout par son incorporation.
      </p>
      <p>
        En tant qu'artiste et chercheur en anthropologie et en psychologie, cette référence philosophique est très proche de mes préoccupations et permet de comprendre le caractère un peu borderline2  (limite) des expériences artistiques que je mène avec mon conjoint Benoît Mangin, dans lesquelles le vivant et le corps humain en particulier sont toujours en jeu. Car la question qui me taraude depuis toujours est « quelles sont les limites de ma conscience incarnée ? », et ma conviction demeure que le langage artistique non verbal est un outil d'introspection active de ce champ expérimental.
      </p>
      <p>
        Nécessairement chercher les limites de son propre entendement peut conduire à représenter un état considéré comme extrême pour l’autre, et de ce fait inacceptable. La psychologie clinique sait combien la notion d'inacceptable vient le plus souvent de l'impossibilité qu'a un esprit à se représenter une évocation qu'il qualifie conséquemment comme telle. C'est le phénomène bien connu de Marguerite Duras évoquant Hiroshima, où l'inacceptable se confond avec l'illusion, « j'ai eu une illusion devant Hiroshima que jamais je n'oublierai »3. Or la mise en forme artistique autorise la présentation d'un certain inacceptable, ce qui ne signifie pas forcément un « immonde ». Ce faisant elle permet d'élargir l'entendement que nous pouvons avoir de notre environnement.
      </p>
      <a class="pic-ref" href="#img-2" to="#img-2">[FIG 2]</a>
      <p>
        C'est en 1985 que je fis les premières biopsies de ma peau dans le laboratoire de ma mère au centre hospitalier universitaire de Lariboisière à Paris. Soigneusement j'ai déposé ces échantillons de moi-même dans des petites cavités pratiquées à la surface de cubes de cire. J'étais alors une jeune étudiante, et déjà je considérais ces objets comme une hybridation artistique. Ce que j'étais la seule à considérer alors. Les autres n'y voyaient qu'un artefact un peu dégoûtant. Certes d'une esthétique blanche, translucide, assez pure. Mais il ne s'agissait toujours pour eux que d'un résidu biologique. Pour moi, il s'agissait déjà de la mise en forme d’une expérience vécue m'ayant permis d'observer à distance un morceau de moi-même. D'une certaine manière une expérience de conscience extracorporelle. Expérience finalement très familière dans notre culture chrétienne qui a véhiculé pléthore d'images mystiques de corps éclatés propres à fasciner les croyants : Sainte-Lucie de Syracuse pourtant ses yeux sur un plateau, Sainte Agathe ses seins découpés, Saint-Denis décapité tenant sa tête entre ses mains, etc.4 « C'est seulement parce qu'il existe un premier refoulement de nos expériences du monde que nous pouvons commencer à les penser » nous dit Serge Tisseron5, et justement j'interprétais le dégoût devant ces artefacts comme l'expression d'un refoulement fondateur d'une nouvelle conscience corporelle. Le fait que mon corps d’enfant fragile fut un perpétuel objet d'études médicales n’était forcément pas étranger à cette mise en œuvre adolescente.
      </p>
